class Car {
    constructor(size, plate) {
        this.size = size;
        this.plate = plate;
    }

    setSize(size) {
        this.size = size;
    }

    getSize() {
        return this.size;
    }

    setPlate(plate) {
        this.plate = plate;
    }

    getPlate() {
        return this.plate;
    }
}